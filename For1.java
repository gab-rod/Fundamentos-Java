package controle;

public class For1 {
  
	public static void main(String[] args) {

// Este programa tem a função de demonstrar a utilização de um for. O contador é disparado somando um número por vez e chegará até 10.
		
		for (int contador = 1; contador <= 10; contador++) {
		System.out.printf("i = %d\n", contador);
		
		}
}
} 