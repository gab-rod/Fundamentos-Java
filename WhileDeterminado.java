package controle;

public class WhileDeterminado {

		public static void main(String[] args) {

//Neste caso utilizarei o While Determinado para imprimir a soma do contador até que chegue na condição determinada.
			
			int contador = 0;
			
			while (contador <= 20) {
			System.out.printf("i = %d\n", contador);
				contador+= 2;
			
			}
		}
}
 