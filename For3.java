package controle;

public class For3 {
	public static void main(String[] args) {

//Neste programa será utilizado um for dentro do outro, com esse exemplo mostrarei como os laços trabalham em conjunto.
		
		for (int i = 0; i <10; i++) {
		for (int j = 0; j <10; j++) { 
		
		System.out.printf(" [%d %d]", i, j);
	}
		System.out.println();
		
		
		}
	} 
}
